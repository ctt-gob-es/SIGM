package com.ieci.tecdoc.common.isicres;

public class PersonAddressTel {
	private String Id = "";
	private String DirTel = "";
	private String Type = "";
	private String Preference = "";
	private String toDelete = "";
	/**
	 * @return the id
	 */
	public String getId() {
		return Id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		Id = id;
	}
	/**
	 * @return the dirTel
	 */
	public String getDirTel() {
		return DirTel;
	}
	/**
	 * @param dirTel the dirTel to set
	 */
	public void setDirTel(String dirTel) {
		DirTel = dirTel;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return Type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		Type = type;
	}
	/**
	 * @return the preference
	 */
	public String getPreference() {
		return Preference;
	}
	/**
	 * @param preference the preference to set
	 */
	public void setPreference(String preference) {
		Preference = preference;
	}
	/**
	 * @return the toDelete
	 */
	public String getToDelete() {
		return toDelete;
	}
	/**
	 * @param toDelete the toDelete to set
	 */
	public void setToDelete(String toDelete) {
		this.toDelete = toDelete;
	}
	
	
}
